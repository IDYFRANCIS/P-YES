public class History {
private String academicQualification;
private String applicantProgramName;
private String beneficiaryStatus;
private String employmentStatus;

public String getAcademicQualification() {
return academicQualification;
}

public void setAcademicQualification(String academicQualification) {
this.academicQualification = academicQualification;
}

public String getApplicantProgramName() {
return applicantProgramName;
}

public void setApplicantProgramName(String applicantProgramName) {
this.applicantProgramName = applicantProgramName;
}

public String getBeneficiaryStatus() {
return beneficiaryStatus;
}

public void setBeneficiaryStatus(String beneficiaryStatus) {
this.beneficiaryStatus = beneficiaryStatus;
}

public String getEmploymentStatus() {
return employmentStatus;
}

public void setEmploymentStatus(String employmentStatus) {
this.employmentStatus = employmentStatus;
}
    
}
